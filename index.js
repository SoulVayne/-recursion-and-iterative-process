// 1. Напишите функцию, которая рекурсивно считает сумму чисел от 10 до n (n > 10).

function sumFrom10ToN(n) {
    if (n === 10) {
      return 10;
    }
    return n + sumFrom10ToN(n - 1);
}


// 2. Напишите функцию fib(n), которая по данному целому неотрицательному числу n возвращает n-ое число Фибоначчи.
// Напоминаю, что последовательность чисел Фибоначчи — это последовательность чисел, в которой первые два числа равны 0 и 1, а каждое последующее число равно сумме двух предыдущих чисел.
// Последовательность начинается так: 0, 1, 1, 2, 3, 5, 8, 13, ...

function fib(n) {
    if (n <= 1) {
      return n;
    }
    return fib(n - 1) + fib(n - 2);
  }

// 3. Умножить все целые положительные нечётные числа в диапазоне от n до m включительно. Известно, что n < m.

function multiplyOddNumbers(n, m) {
    let result = 1;
    for (let i = n; i <= m; i += 1) {
      if (i % 2 !== 0) {
        result *= i;
      }
    }
    return result;
  }


// 4. На вход подается действительное положительное число а и целое неотрицательное число n.
// Вычислите а в степени n. Решите и оформите в виде функции power(a,n).

function power(a, n) {
    let result = 1;
    for (let i = 0; i < n; i += 1) {
      result *= a;
    }
    return result;
  }

