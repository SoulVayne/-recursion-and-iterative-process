// Функция должна корректно обрабатывать входные данные.
// Для неположительных чисел (включая 0) и нечисловых значений функция должна возвращать 0.

function sumFibonacci(n) {
    if (n <= 0) {
      return 0;
    }
    let sum = 0;
    let prev = 0;
    let curr = 1;
    for (let i = 1; i <= n; i += 1) {
      sum += curr;
      const next = prev + curr;
      prev = curr;
      curr = next;
    }
    return sum;
  }

